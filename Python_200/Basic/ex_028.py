# Ex028 - Slicing sequence datatype

strdata = 'Time is money!!'
print(strdata[1:5])     #'ime' is printed out
print(strdata[:7])      #'Time is' is printed out
print(strdata[9:])      #'oney!!' is printed out
print(strdata[:-3])     #'Time is mone' is printed out
print(strdata[-3:])     #'y!!' is printed out
print(strdata[:])       #'Time is money!!' is printed out
print(strdata[::2])     #'Tm smny!' is printed out

# Ex027 - Indexing sequence datatypes

strdata = 'Time is money!!'
listdata = [1, 2, [1, 2, 3]]
print(strdata[5])       # 'i' is printed out
print(strdata[-2])      # '!' is printed out
print(listdata[0])      # 1 is printed out
print(listdata[-1])     # [1,2,3] is printed out
print(listdata[2][-1])  # 3 is printed out

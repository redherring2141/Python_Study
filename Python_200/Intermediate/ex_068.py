# Ex068 - Rounding values off

ret1 = round(1118)      # Rounding off the numbers to first decimal places
ret2 = round(16.554)    # Rounding off the numbers to first decimal places
ret3 = round(1118, -1)  # Rounding off the numbers to the nearest 1
ret4 = round(16.554, 2) # Rounding off the numbers to second decimal places

print(ret1) #1118
print(ret2) #17
print(ret3) #1120
print(ret4) #16.55

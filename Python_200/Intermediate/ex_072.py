# Ex072 - Getting the maximum and the minimum values (max, min)

listdata = [9.96, 1.27, 5.07, 6.45, 8.38, 9.29, 4.93, 7.73, 3.71, 0.93]
maxval = max(listdata)
minval = min(listdata)
print(maxval)   #9.96 is printed out
print(minval)   #0.93 is printed out

txt = 'Alotofthingsoccureachday'
maxval = max(txt)
minval = min(txt)
print(maxval)   # 'y' is printed out
print(minval)   # 'A' is printed out

maxval = max(2+3, 2*3, 2**3, 3**2)
minval = min('abz', 'a12')
print(maxval)   # 9 is printed out
print(minval)   # 'a12' is printed out
